def rotate(st):
    # print(st)
    n, x, y, a, b = map(lambda x: int(x), st.split())
    arr = list(range(n+1))
    answer = arr[:x] + arr[y:x-1:-1] + arr[y+1:]
    answer = answer[:a] + answer[b:a-1:-1] + answer[b+1:]
    return ' '.join(map(str, answer[1:]))

